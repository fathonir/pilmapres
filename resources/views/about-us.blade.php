							<li>Menyelenggarakan Pemilihan Umum untuk memilih Anggota Dewan Perwakilan Rakyat, Dewan Perwakilan Daerah, Dewan Perwakilan Rakyat Daerah, Presiden dan Wakil Presiden serta Kepala Daerah dan Wakil Kepala Daerah secara langsung, umum, bebas, rahasia, jujur, adil, akuntabel, edukatif dan beradab.</li>
							<li>Meningkatkan kualitas penyelenggaraan Pemilihan Umum yang bersih, efisien dan efektif.</li>
							<li>Melayani dan memperlakukan setiap peserta Pemilihan Umum secara adil dan setara, serta menegakkan peraturan Pemilihan Umum secara konsisten sesuai dengan peraturan perundang-undangan yang berlaku.</li>
							<li>Meningkatkan kesadaran politik rakyat untuk berpartisipasi aktif dalam Pemilihan Umum demi terwujudnya cita-cita masyarakat Indonesia yang demokratis.</li>
